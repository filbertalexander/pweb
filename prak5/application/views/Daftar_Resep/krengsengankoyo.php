    <h2>Bahan:</h2>
<li>300gr daging campur (koyor,urat,tetelan) rebus hingga empuk, potong-potong.</li>
<li>1buah bawang bombay,rajang.</li>
<li>1batang daun bawang,rajang.</li>

<h3>Bumbu :</h3>
<li>1sdm margarin.</li>
<li>2sdm baceman bawang.</li>
<li>1ruas kecil jahe,geprek.</li>
<li>1sdt lada hitam (pakai lada putih boleh).</li>
<li>15cabe rawit (sesuai selera),rebus,lalu haluskan.</li>
<li>1sdm kecap inggris.</li>
<li>1sdt kecap asin.</li>
<li>4sdm kecap manis (sesuai selera).</li>
<li>1sdt minyak wijen.</li>
<li>Gula,garam,kaldu bubuk secukupnya.</li>

<h3>Cara memasak :</h3>
<ol>
  <li>Panaskan wajan,tumis baceman bawang bersama margarin,bawang bombay dan jahe hingga harum.</li>
  <li>Beri air secukupnya,masukkan potongan daging yang telah direbus.</li>
  <li>Masukkan ulekan cabe dan bumbui dengan kecap inggris,kecap asin,kecap manis,gula,garam,lada hitam dan juga kaldu bubuk aduk-aduk hingga rata.</li>
  <li>Kecilkan api,tunggu hingga bumbu meresap.</li>
  <li>Terakhir koreksi rasa,taburi dengan irisan daun bawang dan juga minyak wijen,aduk sebentar lalu angkat dan sajikan bersama nasi panas.</li>
</ol>

