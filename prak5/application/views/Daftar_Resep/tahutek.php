    <h3>Bahan :</h3>
<li>Tahu putih.</li>
<li>2 Telor.</li>
<li>2 genggam kacang tanah goreng / -+100gr.</li>
<li>4 siung bw putih.</li>
<li>Sedikit lada.</li>
<li>Garam.</li>
<li>Cabe rawit jika suka pedas.</li>
<li>1/2sdm petis udang.</li>
<li>1-2sdm kecap manis.</li>
<li>Daun seledri iris tipis2.</li>
<li>Kerupuk bawang yg biasanya juga untuk gado2.</li>
<li>2 genggam Toge rebus.</li>
<li>Lontong atau nasi.</li>

<h3>Cara memasak :</h3>
<li>Bumbu/saus kacang</li>
<li>Goreng kacang tanah hingga matang, boleh digoreng bersama bawang putih dan cabe rawit, sisihkan.</li>
<li>Jika sudah dingin blender semuanya ditambah air matang agar blender tidak berat jalannya.</li>
<li>Tuang di mangkok, tambahkan gula, garam, kecap manis, dan petis udang.</li>
<li>Aduk rata. Boleh ditambah kecap sampai dapat rasa dan warna saus yg diinginkan.</li>

<h3>Tahu telor:</h3>
<li>Kocok lepas telor.</li>
<li>Masukkan tahu yg sdh diiris kotak2 kecil tambahkan garam, lada, dan sedikit kaldu bubuk.</li>
<li>Goreng dengan wajan cekung dengan minyak yang panas dan api cenderung. kecil agar tahu matang sempurna.</li>


<h3>Plating:</h3>
<li>Iris lontong/nasi tata d piring, letakkan tahu telornya.</li>
<li>Siram sausnya.</li>
<li>Tambahkan toge dan terakhir irisan daun seledrinya.</li>
<li>Tambahkan timun jika suka Jangan lupa kerupuknya.</li>
<li>Sajikan selagi hangat.</li>

